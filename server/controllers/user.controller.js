const db = require('../models');
const User = db.users;

exports.createNewUser = (req, res) => {
    // Request Validation
    if (!req.body.email) {
        res.status(400).send({message: "Email field cannot be empty"})
        return;
    } else if (!req.body.username) {
        res.status(400).send({message: "Username field cannot be empty"})
        return;
    } else if (!req.body.password) {
        res.status(400).send({message: "Password field cannot be empty"})
        return;
    } 
    // Create a new User
    const newUser = new User({
        email: req.body.email,
        username: req.body.username,
        password: req.body.password
    });

    // Add the new User to the database
    newUser
        .save(newUser)
        .then(data => {
            res.send(data);
        })
        .catch(err => {
            res.status(500).send({
                message:
                err.message || "An error occured while adding a new User"
            });
        });
};

